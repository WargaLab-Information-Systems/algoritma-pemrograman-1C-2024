# Materi Variabel
# Variabel adalah tempat menyimpan nilai. Contoh:
nama = "Putra"
umur = 26
print(f"Nama: {nama}, Umur: {umur}")

# Tipe Data
# Tipe data di Python bisa berupa:
# - Integer (bilangan bulat)    
# - Float (bilangan desimal)
# - String (teks)
# - Boolean (True/False)

# Contoh Tipe Data
bilangan_bulat = 10          # Integer
bilangan_desimal = 10.5      # Float
teks = "Belajar Python"      # String
status = True                 # Boolean

print(f"Integer: {bilangan_bulat}, Float: {bilangan_desimal}, String: {teks}, Boolean: {status}")
